const autocompleteScore = lines
  .filter((l) => getIllegalChar(l) == null)
  .map(getAutocompleteChars)
  .map(getAutocompleteScore)
  .sort((a, b) => a - b);

console.log(autocompleteScore[Math.floor(autocompleteScore.length / 2)]);

function getIllegalChar(line) {
  const stack = [];
  for (const char of line) {
    if (!isClosingChar(char)) {
      stack.push(char);
      continue;
    }
    const lastOpeningChar = stack[stack.length - 1];
    if (!charsMatch(lastOpeningChar, char)) {
      return char;
    }
    stack.pop();
  }
  return null;
}

function isClosingChar(c) {
  return [")", "]", "}", ">"].indexOf(c) > -1;
}

function charsMatch(o, c) {
  switch (o) {
    case "(":
      return c === ")";
    case "[":
      return c === "]";
    case "{":
      return c === "}";
    case "<":
      return c === ">";
  }
  return false;
}

function getAutocompleteChars(line) {
  const stack = [];
  for (const char of line) {
    if (!isClosingChar(char)) {
      stack.push(char);
      continue;
    }
    const lastOpeningChar = stack[stack.length - 1];
    if (!charsMatch(lastOpeningChar, char)) {
      return char;
    }
    stack.pop();
  }

  const closingChars = [];
  while (stack.length > 0) {
    const openingChar = stack.pop();
    closingChars.push(getClosingChar(openingChar));
  }

  return closingChars;
}

function getAutocompleteScore(chars) {
  let score = 0;
  for (const char of chars) {
    score *= 5;
    score += autocompleteScoresByChar.get(char);
  }
  return score;
}

function getClosingChar(o) {
  switch (o) {
    case "(":
      return ")";
    case "[":
      return "]";
    case "{":
      return "}";
    case "<":
      return ">";
  }
}
