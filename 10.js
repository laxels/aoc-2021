const checkerScoresByChar = new Map([
  [")", 3],
  ["]", 57],
  ["}", 1197],
  [">", 25137],
]);

const autocompleteScoresByChar = new Map([
  [")", 1],
  ["]", 2],
  ["}", 3],
  [">", 4],
]);

const checkerScore = lines
  .map(getIllegalChar)
  .filter((c) => c)
  .map((c) => checkerScoresByChar.get(c))
  .reduce((total, points) => total + points, 0);

